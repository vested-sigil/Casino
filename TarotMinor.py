# Functions to get card interpretations
def get_minor_arcana_interpretation(card, orientation):
    card = card.strip()
    if card in minor_arcana_interpretations:
        if orientation == 'upright':
            return minor_arcana_interpretations[card]['upright']
        elif orientation == 'reversed':
            return minor_arcana_interpretations[card]['reversed']
    return ["Invalid card or orientation."]

